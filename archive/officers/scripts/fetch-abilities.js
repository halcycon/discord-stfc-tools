const https = require('https');
const fs = require('fs');

function httpsGet(url) {
    return new Promise((resolve, reject) => {
        https.get(url, (res) => {
            let data = '';
            res.on('data', (chunk) => {
                data += chunk;
            });
            res.on('end', () => {
                try {
                    resolve(JSON.parse(data));
                } catch (e) {
                    reject(e);
                }
            });
        }).on('error', reject);
    });
}

async function fetchAbilities() {
    console.log('Fetching ability data from STFC API...');
    
    try {
        // Fetch all ability data
        const abilityData = await httpsGet('https://api.spocks.club/ability');
        console.log(`Fetched ${abilityData.length} abilities`);
        
        // Create a mapping of ability ID to description
        const abilityDescriptions = {};
        
        abilityData.forEach(ability => {
            // Look for ability descriptions in the localization data
            if (ability.loca_id) {
                // Try to find English text for this ability
                const englishKeys = Object.keys(ability).filter(key => 
                    key.startsWith('ability_desc_') && key.includes('_en')
                );
                
                if (englishKeys.length > 0) {
                    // Use the first English description found
                    abilityDescriptions[ability.loca_id] = ability[englishKeys[0]];
                } else {
                    // Try any description field
                    const descKeys = Object.keys(ability).filter(key => 
                        key.includes('desc') || key.includes('name')
                    );
                    
                    if (descKeys.length > 0) {
                        abilityDescriptions[ability.loca_id] = ability[descKeys[0]];
                    }
                }
            }
        });
        
        console.log(`Found descriptions for ${Object.keys(abilityDescriptions).length} abilities`);
        
        // Let's also check what properties exist in the ability data
        if (abilityData.length > 0) {
            console.log('Sample ability properties:', Object.keys(abilityData[0]));
        }
        
        // Generate TypeScript file with ability descriptions
        const tsContent = `// Auto-generated from api.spocks.club/ability
// Do not edit this file manually - run 'node fetch-abilities.js' to regenerate

export const ABILITY_DESCRIPTIONS: Record<number, string> = ${JSON.stringify(abilityDescriptions, null, 2)};

export function getAbilityDescription(locaId: number): string {
    return ABILITY_DESCRIPTIONS[locaId] || \`Unknown ability (ID: \${locaId})\`;
}
`;
        
        fs.writeFileSync('./src/abilityDescriptions.ts', tsContent);
        console.log('✅ Generated src/abilityDescriptions.ts');
        
    } catch (error) {
        console.error('❌ Error fetching abilities:', error.message);
        process.exit(1);
    }
}

fetchAbilities();
